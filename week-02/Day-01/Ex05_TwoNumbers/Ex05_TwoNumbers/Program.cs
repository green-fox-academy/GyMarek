using System;

namespace Ex05_TwoNumbers
{
    class Program
    {
        static void Main(string[] args)
        {
            int a = 13;
            int b = 22;

            Console.WriteLine(a + b);
            Console.WriteLine(b - a);
            Console.WriteLine(a * b);
            Console.WriteLine(22.0 / 13.0);
            Console.WriteLine(b / a);
            Console.WriteLine(b % a);

            Console.ReadLine();
        }
    }
}
